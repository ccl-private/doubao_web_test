export function changeLanguage(lang) {
    console.log('Changing language to:', lang);
    console.log('Available translations:', Object.keys(translations));

    if (!translations[lang]) {
        console.error('No translations available for:', lang);
        return;
    }

    // 处理所有带有data-i18n属性的元素
    const elements = document.querySelectorAll('[data-i18n]');
    console.log('Found elements with data-i18n:', elements.length);

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        console.log('Processing key:', key);

        if (translations[lang][key]) {
            // 处理HTML内容（允许包含HTML标签）
            element.innerHTML = translations[lang][key];
        } else {
            console.warn('Missing translation for key:', key, 'in language:', lang);
        }
    });

    // 处理所有带有data-i18n-placeholder属性的元素
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    console.log('Found elements with data-i18n-placeholder:', placeholders.length);

    placeholders.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        console.log('Processing placeholder key:', key);

        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        } else {
            console.warn('Missing translation for placeholder key:', key, 'in language:', lang);
        }
    });

    // 处理select选项
    const options = document.querySelectorAll('option[data-i18n]');
    console.log('Found options with data-i18n:', options.length);

    options.forEach(option => {
        const key = option.getAttribute('data-i18n');
        console.log('Processing option key:', key);

        if (translations[lang][key]) {
            option.textContent = translations[lang][key];
        } else {
            console.warn('Missing translation for option key:', key, 'in language:', lang);
        }
    });
}